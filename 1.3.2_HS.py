#17a, hypotenuse calculator
def hyp (leg1, leg2):
    return ((leg1 ** 2 + leg2 ** 2) ** 0.5) # Use the Pythagorean theorem to calculate the hypotenuse 
    # from right triangle sides and after take a square root of sum of squares

#17b, mean calculator
def mean (a, b, c): 
    return ((a + b + c)/3.0) # Average (MEAN) of three numbers
   
#17c, perimeter calculator
def perimeter (base, height):
    return (base + height) * 2 # After adding the base and the height, 
    # the code will multiply by 2 b/c there are two heights and two bases 

